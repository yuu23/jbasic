package fundamentals.chapter2.sec2_variable;

/*
 * 2つの整数「x」「y」を定義して、その積と商を求めて表示するプログラムを作成してください。
 * x,yには任意の整数を代入して実施してください。
 */
public class No010 {
    public static void main(String[] args) {
    	int x = 10;
    	int y = 2;

    	System.out.println(x * y);
    	System.out.println(x / y);
    }
}