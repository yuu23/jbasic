package fundamentals.chapter2.sec2_variable;
/*
 * 次の値を入れるために、最適なデータ型と意味をよく表す変数名を考えて、変数の宣言をしてください。
 * また、サンプルデータをそれぞれの変数にリテラルとして入れて画面に出力してください。
 */
public class No013 {
	public static void main(String[] args) {
		int a;
		String b;
		double c;

		a = 98;
		b = "りんご";
		c= 0.08;

		System.out.println("テストの点数「" + a + "」");
		System.out.println("食べ物「" + b + "」");
		System.out.println("消費税率「" + c + "」");
	}
}
